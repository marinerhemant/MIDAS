    def test_nf_spots_match_c(self, nf_work_dir):
        """Run simulateNF and compare spot positions against Python."""
        cfg = nf_work_dir

        # Step 1: Run simulateNF (it calls GetHKLListNF internally)
        result = subprocess.run(
            [str(BUILD_BIN / "simulateNF"),
             str(cfg["param_file"]),
             str(cfg["mic_file"]),
             str(cfg["work"] / "sim_output"),
             "1"],
            cwd=str(cfg["work"]),
            capture_output=True, text=True, timeout=120,
        )
        assert result.returncode == 0, (
            f"simulateNF failed:\n{result.stdout}\n{result.stderr}"
        )

        spots_file = cfg["work"] / "SimulatedSpots.csv"
        assert spots_file.exists(), (
            f"SimulatedSpots.csv not generated. Files in dir: "
            f"{list(cfg['work'].iterdir())}"
        )
        c_spots = parse_simulated_spots_csv(spots_file)
        n_c_entries = len(c_spots["frame_nr"])
        print(f"  C produced {n_c_entries} pixel hits")
        assert n_c_entries > 0, "simulateNF produced no spots"

        # Extract unique spot centers: group by (vox_nr, approximate omega)
        # and take median pixel position
        c_omegas_unique = np.unique(c_spots["omega_raw"])
        c_spot_centers = []
        for ome in c_omegas_unique:
            mask = c_spots["omega_raw"] == ome
            c_spot_centers.append({
                "omega": ome,
                "hor_px": np.median(c_spots["hor_px"][mask]),
                "ver_px": np.median(c_spots["ver_px"][mask]),
                "frame_nr": int(np.median(c_spots["frame_nr"][mask])),
            })
        n_c_spots = len(c_spot_centers)
        print(f"  C unique spots (by omega): {n_c_spots}")

        # Step 2: Parse hkls.csv (generated by GetHKLListNF inside simulateNF)
        hkls_file = cfg["work"] / "hkls.csv"
        assert hkls_file.exists(), "hkls.csv not generated"
        hkls_cart, thetas, hkls_int, _ = parse_hkls_csv(hkls_file)
        print(f"  {hkls_cart.shape[0]} HKL reflections")

        # Step 3: Run Python forward model with triangular voxels
        geometry = HEDMGeometry(
            Lsd=cfg["Lsd"], y_BC=cfg["y_bc"], z_BC=cfg["z_bc"], px=cfg["px"],
            omega_start=cfg["omega_start"], omega_step=cfg["omega_step"],
            n_frames=cfg["n_frames"],
            n_pixels_y=cfg["n_pix"], n_pixels_z=cfg["n_pix"],
            min_eta=6.0, wavelength=cfg["wl"],
            flip_y=False,  # NF convention
        )
        model = HEDMForwardModel(
            hkls=hkls_cart, thetas=thetas, geometry=geometry,
            hkls_int=hkls_int, device=torch.device("cpu"),
        )

        # Read Euler angles back from the .mic file to get the EXACT same
        # float64 values that the C code reads (avoids truncation mismatch)
        with open(cfg["mic_file"]) as mf:
            for line in mf:
                if line.startswith("%"):
                    continue
                parts = line.split()
                euler_from_mic = [float(parts[7]), float(parts[8]), float(parts[9])]
                break
        euler = torch.tensor([euler_from_mic], dtype=torch.float64)  # (1, 3)
        centers = torch.tensor(cfg["voxel_pos"][:2], dtype=torch.float64).unsqueeze(0)
        tri = TriVoxelConfig(
            edge_lengths=torch.tensor([cfg["edge_len"]], dtype=torch.float64),
            ud=torch.tensor([cfg["ud"]], dtype=torch.float64),
        )

        py_hits = model.forward_nf_triangles(euler, centers, tri)
        n_py_hits = len(py_hits)
        print(f"  Python produced {n_py_hits} pixel hits (triangular voxels)")

        # Step 4: Compare pixel-for-pixel
        # Build sets of (dist, frame, y, z) for both C and Python
        c_set = set()
        for i in range(len(c_spots["frame_nr"])):
            c_set.add((
                int(c_spots["dist_nr"][i]),
                int(c_spots["frame_nr"][i]),
                int(round(c_spots["hor_px"][i])),
                int(round(c_spots["ver_px"][i])),
            ))

        py_set = set()
        for (vox, d, fr, y, z, ome) in py_hits:
            py_set.add((d, fr, y, z))

        # Compute overlap
        common = c_set & py_set
        only_c = c_set - py_set
        only_py = py_set - c_set

        n_common = len(common)
        n_c = len(c_set)
        n_py = len(py_set)

        match_frac = n_common / n_c if n_c > 0 else 0
        print(f"  C pixels: {n_c}, Python pixels: {n_py}, "
              f"Common: {n_common} ({match_frac:.1%})")
        if only_c:
            sample = list(only_c)[:5]
            print(f"  Only in C (sample): {sample}")
        if only_py:
            sample = list(only_py)[:5]
            print(f"  Only in Python (sample): {sample}")

        assert match_frac > 0.95, (
            f"Only {match_frac:.1%} pixel overlap. "
            f"C={n_c}, Py={n_py}, common={n_common}"
        )


