class TestNFComparison:
    """Compare simulateNF SimulatedSpots.csv against Python model."""

    @pytest.fixture
    def nf_work_dir(self, tmp_path):
        """Create a temp directory with minimal NF-HEDM simulation setup."""
        work = tmp_path / "nf_test"
        work.mkdir()

        # Known orientation
        euler_deg = np.array([45.0, 30.0, 60.0])
        euler_rad = euler_deg * DEG2RAD

        # Single voxel at origin
        voxel_x, voxel_y = 0.0, 0.0
        edge_len = 5.0  # > px (1.48) so triangle rasterization is exercised
        ud = 1.0

        # Lattice: cubic Au
        latc = [4.08, 4.08, 4.08, 90.0, 90.0, 90.0]
        wl = 0.172979
        Lsd = 5000.0  # 5 mm (near-field)
        px_size = 1.48
        y_bc, z_bc = 1024.0, 12.0
        n_pix = 2048
        omega_start = 0.0
        omega_step = 0.25
        n_frames = 1440

        # Write .mic file (single voxel)
        mic_file = work / "test.mic"
        with open(mic_file, "w") as f:
            f.write("%TriEdgeSize 0.500000\n")
