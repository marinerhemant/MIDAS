class TestFFComparison:
    """Compare ForwardSimulationCompressed SpotMatrixGen.csv against Python."""

    @pytest.fixture
    def ff_work_dir(self, tmp_path):
        """Create a temp directory with minimal FF-HEDM simulation setup."""
        work = tmp_path / "ff_test"
        work.mkdir()

        # Known orientation: 3 Euler angles (degrees)
        euler_deg = np.array([45.0, 30.0, 60.0])
        euler_rad = euler_deg * DEG2RAD

        # Compute orientation matrix from Euler angles (ZXZ convention)
        c0, c1, c2 = np.cos(euler_rad)
        s0, s1, s2 = np.sin(euler_rad)
        O = np.array([
            [c0*c2 - s0*c1*s2, -s0*c1*c2 - c0*s2,  s0*s1],
            [s0*c2 + c0*c1*s2,  c0*c1*c2 - s0*s2,  -c0*s1],
            [s1*s2,              s1*c2,               c1],
        ])

        # Grain position
        pos_um = np.array([0.0, 0.0, 0.0])  # at origin for simplicity

        # Lattice: cubic Au
        latc = [4.08, 4.08, 4.08, 90.0, 90.0, 90.0]
        wl = 0.172979
        Lsd = 1000000.0  # 1 meter (far-field)
        px_size = 200.0  # um
        y_bc, z_bc = 1024.0, 1024.0
        n_pix = 2048
        omega_start, omega_step = 0.0, 0.25
        n_frames = 1440  # 360 / 0.25

        # Write GrainsSim.csv
        grain_file = work / "GrainsSim.csv"
        with open(grain_file, "w") as f:
            f.write("%NumGrains 1\n")
            f.write("%BeamCenter 0.0\n")
            f.write("%BeamThickness 10000.0\n")
            f.write("%GlobalPosition 0.0\n")
            f.write("%NumPhases 1\n")
            f.write("%PhaseInfo\n")
            f.write(f"%\tSpaceGroup:225\n")
            lstr = " ".join(f"{v:.6f}" for v in latc)
            f.write(f"%\tLattice Parameter: {lstr}\n")
            f.write("%GrainID\tO11\tO12\tO13\tO21\tO22\tO23\tO31\tO32\tO33"
                    "\tX\tY\tZ\ta\tb\tc\talpha\tbeta\tgamma\n")
            vals = list(O.flatten()) + list(pos_um) + latc
            line = "1\t" + "\t".join(f"{v:.6f}" for v in vals)
            f.write(line + "\n")

        # Write parameter file
        param_file = work / "params_ff.txt"
        with open(param_file, "w") as f:
            f.write(f"LatticeParameter {' '.join(str(v) for v in latc)}\n")
            f.write(f"Wavelength {wl}\n")
            f.write(f"SpaceGroup 225\n")
            f.write(f"Lsd {Lsd}\n")
            f.write(f"BC {y_bc} {z_bc}\n")
            f.write(f"px {px_size}\n")
            f.write(f"NrPixels {n_pix}\n")
            f.write(f"tx 0\n")
            f.write(f"ty 0\n")
            f.write(f"tz 0\n")
            f.write(f"Wedge 0\n")
            f.write(f"OmegaStart {omega_start}\n")
            f.write(f"OmegaEnd {omega_start + omega_step * n_frames}\n")
            f.write(f"OmegaStep {omega_step}\n")
            f.write(f"MaxRingRad {Lsd * 0.3}\n")
            f.write(f"InFileName {grain_file}\n")
            f.write(f"OutFileName {work / 'sim_out'}\n")
            f.write(f"WriteSpots 1\n")
            f.write(f"WriteImage 0\n")
            f.write(f"nScans 1\n")
            f.write(f"PeakIntensity 2000\n")
            f.write(f"GaussWidth 2.0\n")

        return {
            "work": work,
            "param_file": param_file,
            "euler_deg": euler_deg,
            "euler_rad": euler_rad,
            "orient_mat": O,
            "pos_um": pos_um,
            "latc": latc,
            "wl": wl,
            "Lsd": Lsd,
            "px": px_size,
            "y_bc": y_bc,
            "z_bc": z_bc,
            "n_pix": n_pix,
            "omega_start": omega_start,
            "omega_step": omega_step,
            "n_frames": n_frames,
        }

    def test_ff_spots_match_c(self, ff_work_dir):
        """Run ForwardSimulationCompressed and compare spot positions."""
        cfg = ff_work_dir

        # Step 1: Generate HKL list
        result = subprocess.run(
            [str(BUILD_BIN / "GetHKLList"), str(cfg["param_file"])],
            cwd=str(cfg["work"]),
            capture_output=True, text=True, timeout=30,
        )
        assert result.returncode == 0, f"GetHKLList failed:\n{result.stderr}"

        hkls_file = cfg["work"] / "hkls.csv"
        assert hkls_file.exists(), "hkls.csv not generated"
        hkls_cart, thetas, hkls_int, ring_nrs = parse_hkls_csv(hkls_file)
        n_hkls = hkls_cart.shape[0]
        print(f"  {n_hkls} HKL reflections loaded")

        # Step 2: Run C forward simulation
        result = subprocess.run(
            [str(BUILD_BIN / "ForwardSimulationCompressed"),
             str(cfg["param_file"]), "1"],
            cwd=str(cfg["work"]),
            capture_output=True, text=True, timeout=60,
        )
        assert result.returncode == 0, f"ForwardSimulation failed:\n{result.stderr}"

        spot_file = cfg["work"] / "SpotMatrixGen.csv"
        assert spot_file.exists(), "SpotMatrixGen.csv not generated"
        c_spots = parse_spot_matrix_gen(spot_file)
        n_c_spots = len(c_spots["omega_deg"])
        print(f"  C produced {n_c_spots} spots")
        assert n_c_spots > 0, "C simulation produced no spots"

        # Step 3: Run Python forward model
        geometry = HEDMGeometry(
            Lsd=cfg["Lsd"], y_BC=cfg["y_bc"], z_BC=cfg["z_bc"], px=cfg["px"],
            omega_start=cfg["omega_start"], omega_step=cfg["omega_step"],
            n_frames=cfg["n_frames"],
            n_pixels_y=cfg["n_pix"], n_pixels_z=cfg["n_pix"],
            min_eta=6.0, wavelength=cfg["wl"],
        )
        model = HEDMForwardModel(
            hkls=hkls_cart, thetas=thetas, geometry=geometry,
            hkls_int=hkls_int, device=torch.device("cpu"),
        )

        euler = torch.tensor(cfg["euler_rad"], dtype=torch.float64).unsqueeze(0)  # (1, 3)
        pos = torch.tensor(cfg["pos_um"], dtype=torch.float64).unsqueeze(0)       # (1, 3)
        spots = model(euler, pos)

        # Extract valid Python spots
        py_omega = spots.omega[spots.valid > 0.5].numpy() * RAD2DEG
        py_eta = spots.eta[spots.valid > 0.5].numpy() * RAD2DEG
        py_y = spots.y_pixel[spots.valid > 0.5].numpy()
        py_z = spots.z_pixel[spots.valid > 0.5].numpy()

        n_py_spots = len(py_omega)
        print(f"  Python produced {n_py_spots} valid spots")
        assert n_py_spots > 0, "Python produced no valid spots"

        # Step 4: Match spots by (omega, ring_nr/eta proximity)
        # For each C spot, find the closest Python spot in omega-eta space
        matched = 0
        omega_errors = []
        eta_errors = []
        y_errors = []
        z_errors = []

        for i in range(n_c_spots):
            c_ome = c_spots["omega_deg"][i]
            c_eta = c_spots["eta_deg"][i]
            c_y = c_spots["det_hor"][i]
            c_z = c_spots["det_vert"][i]

            # Find closest Python spot in (omega, eta) space
            ome_diff = np.abs(py_omega - c_ome)
            eta_diff = np.abs(py_eta - c_eta)
            # Handle eta wrapping
            eta_diff = np.minimum(eta_diff, 360.0 - eta_diff)
            dist = np.sqrt(ome_diff**2 + eta_diff**2)

            best = np.argmin(dist)
            if dist[best] < 1.0:  # within 1 degree in (omega, eta) space
                matched += 1
                omega_errors.append(ome_diff[best])
                eta_errors.append(eta_diff[best])
                y_errors.append(abs(py_y[best] - c_y))
                z_errors.append(abs(py_z[best] - c_z))

        match_frac = matched / n_c_spots if n_c_spots > 0 else 0
        print(f"  Matched {matched}/{n_c_spots} C spots ({match_frac:.1%})")

        if matched > 0:
            print(f"  Omega error: mean={np.mean(omega_errors):.4f} deg, "
                  f"max={np.max(omega_errors):.4f} deg")
            print(f"  Eta error:   mean={np.mean(eta_errors):.4f} deg, "
                  f"max={np.max(eta_errors):.4f} deg")
            print(f"  Y pixel err: mean={np.mean(y_errors):.2f} px, "
                  f"max={np.max(y_errors):.2f} px")
            print(f"  Z pixel err: mean={np.mean(z_errors):.2f} px, "
                  f"max={np.max(z_errors):.2f} px")

        # Assertions
        assert match_frac > 0.90, (
            f"Only {match_frac:.1%} of C spots matched to Python. "
            "Possible convention mismatch."
        )
        if matched > 0:
            assert np.max(omega_errors) < 0.5, (
                f"Max omega error {np.max(omega_errors):.4f} deg exceeds 0.5 deg"
            )
            assert np.max(eta_errors) < 0.5, (
                f"Max eta error {np.max(eta_errors):.4f} deg exceeds 0.5 deg"
            )
            # Pixel errors may be larger due to position/magnification
            # but for FF (Lsd=1m) with grain at origin, should be very small
            assert np.mean(y_errors) < 2.0, (
                f"Mean Y pixel error {np.mean(y_errors):.2f} too large"
            )
            assert np.mean(z_errors) < 2.0, (
                f"Mean Z pixel error {np.mean(z_errors):.2f} too large"
            )


