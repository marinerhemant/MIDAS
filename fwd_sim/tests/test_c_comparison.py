DEG2RAD = math.pi / 180.0
RAD2DEG = 180.0 / math.pi


# ---------------------------------------------------------------------------
#  Helper: parse hkls.csv generated by GetHKLList
# ---------------------------------------------------------------------------
def parse_hkls_csv(path):
    """Parse hkls.csv -> (hkls_cart (M,3), thetas (M,), hkls_int (M,3), ring_nrs (M,))."""
    data = np.loadtxt(path, skiprows=1)
    if data.ndim == 1:
        data = data.reshape(1, -1)
    hkls_int = data[:, 0:3]   # h, k, l integers
    ring_nrs = data[:, 4]     # RingNr
    g_cart = data[:, 5:8]     # g1, g2, g3 (Cartesian reciprocal space)
    theta_deg = data[:, 8]    # Theta in degrees
    return (
        torch.tensor(g_cart, dtype=torch.float64),
        torch.tensor(theta_deg * DEG2RAD, dtype=torch.float64),
        torch.tensor(hkls_int, dtype=torch.float64),
        ring_nrs,
    )


# ---------------------------------------------------------------------------
#  Helper: parse SpotMatrixGen.csv from ForwardSimulationCompressed
# ---------------------------------------------------------------------------
def parse_spot_matrix_gen(path):
    """Parse SpotMatrixGen.csv.

    Returns dict with arrays:
        grain_id, omega_deg, det_hor, det_vert, eta_deg, ring_nr, theta_deg, scan_nr
    """
    data = np.loadtxt(path, skiprows=1, delimiter="\t")
    if data.ndim == 1:
        data = data.reshape(1, -1)
    return {
        "grain_id": data[:, 0].astype(int),
        "omega_deg": data[:, 2],
        "det_hor": data[:, 3],
        "det_vert": data[:, 4],
        "eta_deg": data[:, 6],
        "ring_nr": data[:, 7].astype(int),
        "theta_deg": data[:, 10],
        "scan_nr": data[:, 12].astype(int),
        "ring_rad": data[:, 13],
    }


# ---------------------------------------------------------------------------
#  Helper: parse SimulatedSpots.csv from simulateNF
# ---------------------------------------------------------------------------
def parse_simulated_spots_csv(path):
    """Parse SimulatedSpots.csv from simulateNF.

    Returns dict with arrays:
        vox_nr, dist_nr, frame_nr, hor_px, ver_px, omega_raw
    """
    data = np.loadtxt(path, skiprows=1, delimiter="\t")
    if data.ndim == 1:
        data = data.reshape(1, -1)
    return {
        "vox_nr": data[:, 0].astype(int),
        "dist_nr": data[:, 1].astype(int),
        "frame_nr": data[:, 2].astype(int),
        "hor_px": data[:, 3],
        "ver_px": data[:, 4],
        "omega_raw": data[:, 5],
    }


